public class nestedsample {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            System.out.println("The value of i is " + i);

            for (int j = 0; j < 3; j++) {
                System.out.println("The value of j is " + j);
            }

        }
    }
}
